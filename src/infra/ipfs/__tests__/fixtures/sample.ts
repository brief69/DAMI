// 最小限のJPEGファイルのバイナリデータ
// FFD8 FF - SOI マーカー
// FFD9 - EOI マーカー
export const MINIMAL_JPEG = Buffer.from([
  0xFF, 0xD8, 0xFF, 0xD9
]);

